package com.cavalierfou.chessopenings.customentity;

public class OpeningCustom {

	private long id;

	private String name;

	public OpeningCustom() {
		// default constructor
	}

	public long getId() {
		return this.id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

}